     Apply a single, unique background image with the text "baked in" for each type of button (i.e., "Submit", "Send message") and its rollover state. This approach is consistent across browsers but doesn't scale; you have to create an image for every button in your site, which may not be reasonable to maintain or update. Also, image text is not accessible so a redundant text equivalent must be included in all button markup.<br /><br />
     Tile a background image on the button element. This works really well when you only need rectangular buttons or have the option to use the CSS3 "border-radius" property to apply rounded corners, as demonstrated in our article on styling buttons with the jQuery UI CSS framework. However, it falls short if your design calls for a custom shape, like a polygon, or if rounded corners must be applied consistently across all browsers, in which case you'd need to create a workaround in JavaScript for browsers that don't yet support the border-radius property, like IE.<br /><br />
